###########
# 参照渡し #
###########
j = [1, 2, 3, 4, 5]
# i = &j
i = j
j[0] = 100

# j =  [100, 2, 3, 4, 5]
print("j = ", j)
# j's id =  140684005137600
print("j's id = ", id(j))
# i =  [100, 2, 3, 4, 5]
print("i = ", i)
# i's id =  140684005137600
print("i's id = ", id(i))

#################
# リストのコピー #
#################
x = [1, 2, 3, 4, 5]
y = x.copy()
y[0] = 100

# y =  [100, 2, 3, 4, 5]
print("y = ", y)
# y's id =  139627957696704
print("y's id = ", id(y))
# x =  [1, 2, 3, 4, 5]
print("x = ", x)
# x's id =  139627959847360
print("x's id = ", id(x))