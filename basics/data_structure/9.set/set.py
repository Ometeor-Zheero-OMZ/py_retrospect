########
# 集合 #
########
a = {1, 2, 2, 3, 4, 4, 4, 5, 6}
b = {2, 3, 3, 6, 7}

# {1, 2, 3, 4, 5, 6}
print(a)
# {2, 3, 6, 7}
print(b)

######################################
# a の集合から b の集合の要素を取り除く #
######################################
# {1, 4, 5}
print(a - b)

######################################
# b の集合から a の集合の要素を取り除く #
######################################
# {7}
print(b - a)

##########################################
# a, b の集合どちらにも当てはまる要素を返す #
##########################################
# {2, 3, 6}
print(a & b)

####################################
# a, b の集合どちらかにある要素を返す #
####################################
# {1, 2, 3, 4, 5, 6, 7}
print(a | b)

######################################
# a, b の集合で重複していない要素を返す #
######################################
# {1, 4, 5, 7}
print(a ^ b)