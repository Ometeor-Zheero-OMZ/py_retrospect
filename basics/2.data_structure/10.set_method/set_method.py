#################
# 集合のメソッド #
#################
s = {1, 2, 3, 4, 5}

###################
# 集合に要素を追加 #
###################
s.add(6)

# {1, 2, 3, 4, 5, 6}
print(s)

########################
# 集合から要素を取り除く #
########################
s.remove(6)

# {1, 2, 3, 4, 5}
print(s)

#################
# 空の集合を返す #
#################
s.clear()

# set()
print(s)

